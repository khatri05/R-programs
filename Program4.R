#Common data type in R
#1: Numeric
num <- 10.5
print(num)
print(typeof(num))

#2: Integer
x <- 10
print(x)
print(class(x))

#3: character
name <- "R Language"
print(name)
typeof(name)

#4: Logical
status <- TRUE
print(status)
typeof(status)

#5: Complex
comp <- 2+3i
print(comp)
typeof(comp)

#6: Raw
x <- charToRaw("A")
print(x)
typeof(x)